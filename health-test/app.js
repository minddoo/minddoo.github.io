let currentQ = 0;
let scores = { VIP: 0, QUICK: 0, BEAUTY: 0, INTENSIVE: 0 };
let answerHistory = [];
let currentLang = localStorage.getItem('kmedLang') || 'en';

function changeLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('kmedLang', lang);
    applyTranslations();
    
    if (document.getElementById('screen-question').classList.contains('active')) {
        showQuestion(); // re-render question options
    }
}

function applyTranslations() {
    const t = i18n[currentLang];
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key]) {
            el.innerHTML = t[key];
        }
    });
}

function startTest() {
    document.getElementById('screen-start').classList.remove('active');
    document.getElementById('screen-question').classList.add('active');
    currentQ = 0;
    scores = { VIP: 0, QUICK: 0, BEAUTY: 0, INTENSIVE: 0 };
    answerHistory = [];
    showQuestion();
}

function showQuestion() {
    if (currentQ >= questions.length) {
        showResult();
        return;
    }

    const t = i18n[currentLang];
    const q = questions[currentQ];
    
    document.getElementById('q-num').innerText = currentQ + 1;
    document.getElementById('question-text').innerText = t[q.qKey];

    // Update Progress Bar
    const progress = ((currentQ) / questions.length) * 100;
    document.getElementById('progress-bar').style.width = progress + '%';

    const container = document.getElementById('options-container');
    container.innerHTML = '';

    q.a.forEach((ans) => {
        const btn = document.createElement('button');
        btn.className = 'option-btn';
        btn.innerText = t[ans.textKey];
        btn.onclick = () => selectOption(ans.type);
        container.appendChild(btn);
    });

    // Handle back button visibility
    const backBtn = document.getElementById('btn-back');
    if (backBtn) {
        if (currentQ > 0) {
            backBtn.style.display = 'inline-block';
            backBtn.innerText = t['btnBack'];
        } else {
            backBtn.style.display = 'none';
        }
    }
}

function selectOption(type) {
    scores[type]++;
    answerHistory.push(type);
    currentQ++;
    showQuestion();
}

function goBack() {
    if (currentQ > 0) {
        currentQ--;
        const lastType = answerHistory.pop();
        if (lastType) {
            scores[lastType]--;
        }
        showQuestion();
    }
}

function showResult() {
    let maxType = Object.keys(scores).reduce((a, b) => scores[a] > scores[b] ? a : b);
    localStorage.setItem('healthTestResult', maxType);
    window.location.href = 'result.html';
}

// On load for index.html
document.addEventListener('DOMContentLoaded', () => {
    applyTranslations();
    const langSelect = document.getElementById('lang-select');
    if (langSelect) {
        langSelect.value = currentLang;
        langSelect.addEventListener('change', (e) => {
            changeLanguage(e.target.value);
        });
    }
});
